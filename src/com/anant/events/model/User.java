package com.anant.events.model;

public class User {

	String name;
	String emailId;

	public void setName(String name) {
		this.name = name;
	}

	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}

	public String getName() {
		return name;
	}

	public String getEmailId() {
		return emailId;
	}

}
